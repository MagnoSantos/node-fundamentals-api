"use strict";
const Schema = require("mongoose").Schema;
const schema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = require("mongoose").model("Customer", schema);
